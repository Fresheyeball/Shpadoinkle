-- Name sources: 
-- https://github.com/dominictarr/randomname
-- https://github.com/arineng/arincli/blob/master/lib/lastnames.txt

newtype StockName = StockName { unStockName :: Text }

instance Arbitrary StockName where
  arbitrary = do
    first :: StockFirstName <- arbitrary
    last :: StockLastName <- arbitrary
    return (StockName (pack (show first ++ " " ++ show last)))

instance Arbitrary StockFirstName where
  arbitrary = elements [minBound..maxBound]

instance Arbitrary StockLastName where
  arbitrary = elements [minBound..maxBound]

